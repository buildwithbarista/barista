//! Integration tests for `barista verify` (M4.3 T1).
//!
//! Three flavors of test:
//!
//! 1. **Unit-shaped** tests that exercise the daemon launcher's
//!    discovery + auto-respawn behaviour against fixture-built
//!    daemons. These run on every `cargo test` invocation; they don't
//!    require Maven or a real barback JVM.
//!
//! 2. **`--no-daemon` byte-equal smoke** test that runs
//!    `barista verify --no-daemon` against the same fixture once
//!    via barista's fork and once via bare `mvn verify`, then SHA-256-
//!    diffs every produced `target/**/*.class`. Skipped when `mvn` is
//!    not on `$PATH`.
//!
//! 3. **Cross-language auto-respawn** test that launches a real
//!    `barback Server` JVM with `--crash-after 1` against a synthetic
//!    fixture, drives `barista verify` end-to-end, and asserts the
//!    auto-respawn fires + the action runs through on the retry.
//!    `#[ignore]`-gated like the other JVM conformance suites
//!    (requires Maven + JDK on `$PATH`); run with `--ignored
//!    --test-threads=1`.

use std::fs;
use std::path::{Path, PathBuf};
use std::process::Command;

use barista_cli::action_graph::{VERIFY_PHASE_PREFIX, verify_graph};
use barista_cli::daemon::launcher::{JvmEntry, LaunchPlan, discover_jvm_entry};
use barista_cli::daemon::workers::{available_parallelism_or_one, resolve_workers};

/// Locate the freshly-built `barista` binary. Same pattern as
/// `cmd_no_daemon.rs`.
fn barista_bin() -> PathBuf {
    PathBuf::from(env!("CARGO_BIN_EXE_barista"))
}

fn host_has_mvn() -> bool {
    which::which("mvn").is_ok()
}

