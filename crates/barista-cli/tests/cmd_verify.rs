/// Path to the workspace's `barback/` directory (relative to this
/// crate's `CARGO_MANIFEST_DIR`). Same convention as the existing
/// cross-language tests in `crates/barista-ipc/tests/`.
fn barback_dir() -> PathBuf {
    let manifest = env!("CARGO_MANIFEST_DIR");
    let p = Path::new(manifest).join("../../barback");
    p.canonicalize().unwrap_or(p)
}

/// Build the barback classpath the same way the M4.1 / M4.2
/// conformance helpers do: `mvn test-compile` + `mvn
/// dependency:build-classpath` once, cached in process state.
fn ensure_barback_classpath() -> std::ffi::OsString {
    // `mvn test-compile` first so `target/test-classes` exists.
    let tc = Command::new("mvn")
        .arg("-f")
        .arg(barback_dir().join("pom.xml"))
        .arg("-q")
        .arg("test-compile")
        .status()
        .expect("mvn test-compile spawns");
    assert!(tc.success(), "barback `mvn test-compile` failed");
    let cp = Command::new("mvn")
        .arg("-f")
        .arg(barback_dir().join("pom.xml"))
        .arg("-q")
        .arg("dependency:build-classpath")
        .arg("-Dmdep.outputFile=/dev/stdout")
        .output()
        .expect("mvn dependency:build-classpath spawns");
    assert!(
        cp.status.success(),
        "barback `mvn dependency:build-classpath` failed"
    );
    let resolved = String::from_utf8_lossy(&cp.stdout).trim().to_string();
    let tc_dir = barback_dir().join("target").join("test-classes");
    let mc_dir = barback_dir().join("target").join("classes");
    let mut s = std::ffi::OsString::new();
    s.push(&tc_dir);
    s.push(":");
    s.push(&mc_dir);
    s.push(":");
    s.push(&resolved);
    s
}

#[test]
#[ignore = "requires Maven + JDK on PATH; run with --ignored"]
fn auto_respawn_against_crashing_barback() {
    // Build a fixture daemon spawn at a tempdir socket. The daemon
    // gets `--crash-after 1` so it self-halts after dispatching the
    // first action envelope; our auto-respawn driver should observe
    // BAR-DAEMON-CRASHED, kill the leftover PID, spawn a fresh
    // daemon, and complete action #2.
    //
    // This is the same fixture pattern as
    // `crates/barista-ipc/tests/crash_recovery_conformance.rs`,
    // generalised to drive it through the M4.3 T1 launcher +
    // respawn loop.

    let td = tempfile::tempdir().unwrap();
    let socket_dir = td.path().join("run");
    fs::create_dir_all(&socket_dir).unwrap();
    let mut plan = LaunchPlan::new(socket_dir.clone(), 1, 60);
    plan.crash_after = Some(1);
    plan.spawn_timeout = std::time::Duration::from_secs(30);

    let cp = ensure_barback_classpath();
    let entry = JvmEntry::Classpath {
        classpath: cp,
        main_class: "com.bluminal.barista.barback.Server".to_string(),
    };

    let h =
        barista_cli::daemon::discover_or_spawn(&plan, || Ok(entry.clone())).expect("initial spawn");

    // Submit action #1 — daemon halts after dispatching it. The
    // auto-respawn driver detects BAR-DAEMON-CRASHED and respawns.
    // The retry's daemon (spawned via the same plan) is FRESH —
    // it doesn't carry crash_after into its second life because
    // the plan is mutated by the test owner, not by the launcher.
    let mut plan_retry = plan.clone();
    plan_retry.crash_after = None; // fresh daemon has no crash arming
    let req = barista_ipc::ActionRequest::default();
    let outcome = barista_cli::daemon::submit_with_respawn(&plan_retry, h, &entry, req);

    // The daemon's BAR-DAEMON-NOT-YET-IMPLEMENTED stub returns
    // status=FAILURE / exit_code=1 for any action it dispatches;
    // that's an expected non-crash outcome, NOT a respawn trigger.
    // What we're testing here is the *crash trigger* path:
    //
    //   * action #1 trips --crash-after 1 → JVM halt(137) → respawn
    //   * action #2 (retry) hits the fresh daemon → returns
    //     BAR-DAEMON-NOT-YET-IMPLEMENTED → outcome.result.exit_code = 1
    //     and outcome.respawns == 1.
    //
    // We accept either:
    //   (a) Ok((outcome, _)) with respawns >= 1, or
    //   (b) Err(RespawnError::PersistentCrash) if the retry's daemon
    //       also somehow crashed (shouldn't happen with crash_after=None).
    //
    // (a) is the AC; (b) is a flake-tolerant escape hatch.
    match outcome {
        Ok((o, _h)) => {
            assert!(
                o.respawns >= 1,
                "auto-respawn should have fired at least once; got respawns={}",
                o.respawns,
            );
        }
        Err(barista_cli::daemon::respawn::RespawnError::PersistentCrash) => {
            // Tolerated — see comment above.
            eprintln!("note: persistent crash (retry daemon also crashed); accepted as flake.");
        }
        Err(e) => panic!("unexpected error from auto-respawn driver: {e}"),
    }
}
