#[test]
fn action_graph_verify_phase_prefix_is_exhaustive() {
    // M4.3 T1 ACs require the lifecycle prefix
    // `process-resources … verify`. Pin the list here so a future
    // accidental drop / reorder fails loudly with the ACs in the
    // error message.
    assert_eq!(
        VERIFY_PHASE_PREFIX,
        &[
            "process-resources",
            "compile",
            "process-test-resources",
            "test-compile",
            "test",
            "prepare-package",
            "package",
            "integration-test",
            "verify",
        ],
        "M4.3 T1 AC: verify lifecycle phase prefix must match Maven's default lifecycle (mvn help:describe -Dcmd=verify).",
    );
}

#[test]
fn action_graph_targets_single_module_pom() {
    let project = PathBuf::from("/tmp/barista-test-project");
    let g = verify_graph(project.clone(), false);
    assert_eq!(g.module_root, project);
    assert_eq!(g.pom_path, project.join("pom.xml"));
    // M4.3 T1 is sequential single-module; the graph carries
    // exactly one action per phase, ordered.
    assert_eq!(g.actions.len(), VERIFY_PHASE_PREFIX.len());
}

#[test]
fn workers_resolver_honors_1c_default() {
    let cores = available_parallelism_or_one();
    let n = resolve_workers("1C", cores).expect("1C must resolve");
    assert_eq!(n, cores.get(), "1C must equal core count");
}

#[test]
fn workers_resolver_rejects_garbage() {
    let cores = available_parallelism_or_one();
    let err = resolve_workers("not-a-number", cores).unwrap_err();
    assert!(err.to_string().contains("malformed"), "got: {err}",);
}

